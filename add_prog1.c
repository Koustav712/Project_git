// write a program to find the addition of two numbers


#include<stdio.h>
int main()
{
	int num1,num2,add;
	printf("The value of first number : ");
	scanf("%d",&num1);
	printf("The value of second number : ");
	scanf("%d",&num2);
	add=num1+num2;
	printf("The addition of two numbers is : %d",add);
	return 0;
}
